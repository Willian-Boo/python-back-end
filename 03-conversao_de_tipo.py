#criando uma variavel com o valor 10
preco = 10
print(preco)

#dividindo o 10/2 irá retornar 5.0
print(preco / 2)

#operador para retorna o numero inteiro de uma divisão, retorno será 5
print(preco // 2)

preco = 10.50

#converter a variavel preco para inteiro, retorno será 10
print(int(preco))

#atribuindo o tipo str para a variavel preco
preco = "2.8"

#exibe o tipo da variavel, retorno será <class 'str'>
print(type(preco))