valor_1 = 7
valor_2 = 2

#adição
print(f"a soma de {valor_1} + {valor_2} é :", valor_1 + valor_2)

#subtração
print(f"a subtração de {valor_1} - {valor_2} é :", valor_1 - valor_2)

#multiplicaçao
print(f"a multiplicação de {valor_1} + {valor_2} é :", valor_1 * valor_2)

#divisão
print(f"a divisão de {valor_1} / {valor_2} é :", valor_1 / valor_2)

#divisão sem casas decimais
print(f"a divisão sem casas decimais de {valor_1} // {valor_2} é :", valor_1 // valor_2)

#exponenciação
print(f"a potencia de {valor_1} ** {valor_2} é :", valor_1 ** valor_2)

#modulo
print(f"o resto da divisão de {valor_1} % {valor_2} é :", valor_1 % valor_2)