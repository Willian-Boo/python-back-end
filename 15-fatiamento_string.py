nome = "Willian Borges Silva"

#exibir apenas a primeira letra
print(nome[0])

#exibir a última letra
print(nome[-1])

#exibir do inicio do texto até 4 letras seguintes
print(nome[:4])

#começa a exibir o texto após 7 letras
print(nome[7:])

#passando intervalos de inicio e fim
print(nome[8:14])

#intervalo de inicio e fim, com step de 2 em 2
print(nome[8:14:2])

#retorna uma cópia da string
print(nome[:])

#retorna o texto invertido
print(nome[::-1])
